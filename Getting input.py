# Python can be used to recive input from the user
# Get user's name
name = input("What is your name?\n")
print("Hi " + name)

# Get user's favorite color
color = input("What is your favorite color?\n")
print(name + " likes " + color)

# Get user's favorite food
food = input("What is your favorite food?\n")
print(name + " likes " + food)

# Get user's Date of birth
birth = int(input("What is your Date of Birth?\n"))
print(name + " was born in " + str(birth))
